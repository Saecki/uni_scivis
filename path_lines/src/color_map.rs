pub const SIZE: usize = 256;

#[derive(Clone, Debug)]
pub struct ColorMap([[u8; 4]; SIZE]);

impl ColorMap {
    pub fn as_slice(&self) -> &[[u8; 4]] {
        &self.0
    }
}

pub const GRAY: ColorMap = {
    let mut buf = [[0u8; 4]; SIZE];
    let mut i = 0;
    while i < SIZE {
        let gray = i as u8;
        buf[i] = [gray, gray, gray, 255];
        i += 1;
    }
    ColorMap(buf)
};

